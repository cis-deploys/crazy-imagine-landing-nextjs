export const colors = variant => {
  if (variant === "primary") {
    return "#23aae1"
  } else if (variant === "secondary") {
    return "white"
  } else {
    return "black"
  }
}

export const colorsIconos = variantIcons => {
  if (variantIcons === "primary") {
    return "#23aae1"
  } else if (variantIcons === "secondary") {
    return "white"
  } else {
    return "black"
  }
}
